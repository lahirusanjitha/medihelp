    public function Locationtypeedit(){
        $recordID=$this->input->post('recordID');

        $this->db->select('*');
        $this->db->from('tbl_location_type');
        $this->db->where('idtbl_location_type', $recordID);
        $this->db->where('status', 1);

        $respond=$this->db->get();

        $obj=new stdClass();
        $obj->id=$respond->row(0)->idtbl_location_type;
        $obj->location_type=$respond->row(0)->location_type;

        echo json_encode($obj);
    }
