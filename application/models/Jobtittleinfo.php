        $this->db->from('tbl_job_tittle');
        $this->db->where('tblid_job_tittle', $recordID);
        $this->db->where('status', 1);

        $respond=$this->db->get();

        $obj=new stdClass();
        $obj->id=$respond->row(0)->tblid_job_tittle;
